using UnityEngine;

public class PlayerPawn : Pawn
{
    public SkullMover mover;
    //------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------  
    // Start is called once before the first execution of Update after the MonoBehaviour is created
    void Start()
    {
        mover = GetComponent<SkullMover>(); // Makes sure that the SkullMover component is available to the PlayerPawn.
    }
//------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------
    // Update is called once per frame
    void Update()
    {
        
    }
//------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------
    public override void MoveForward() // Capital M in Pawn - helping to match the names so I don't keep getting them mixed up.
    {
        if (mover != null) // If the mover exists, carry out moveForward();
        {
            mover.moveForward(); // Lowercase m in SkullMover
            
        }
    }
//------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------
    public override void MoveBackward() // M
    {
        if (mover != null)
        {
            mover.moveBackward(); // m
        }
    }
//------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------
    public override void RotateClockwise() // Upper Case R
    {
        if (mover != null)
        {
            mover.rotateClockwise(); // lowercase r
        }
    }
//------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------
    public override void RotateCounterclockwise() // Upper Case R
    {
        if (mover != null)
        {
            mover.rotateCounterclockwise(); // lowercase r
        }
    }
//------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------
    public override void MoveUp() // M
    {
        if (mover != null)
        {
            mover.moveUp(); // m
        }
    }
//------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------
    public override void MoveDown() // M
    {
        if (mover != null)
        {
            mover.moveDown(); // m
        }
    }
//------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------
    public override void MoveLeft() // M
    {
        if (mover != null)
        {
            mover.moveLeft(); // m
        }
    }
//------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------
    public override void MoveRight() // M
    {
        if (mover != null)
        {
            mover.moveRight(); // m
        }
    }
//------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------
    public override void Turbo() // M
    {
        if (mover != null)
        {
            mover.turbo(); // m
        }
    }
//------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------
    public override void Teleport()
    {
        mover.Teleport(minX, maxX, minY, maxY); // defining minX, maxX etc...
    }
//------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------
}

